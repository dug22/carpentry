package io.github.dug22.carpentry.io;

import io.github.dug22.carpentry.DefaultDataFrame;

import java.nio.file.Path;

public abstract class SourceReader {

    protected Path filePath;
    protected String urlString;

    public SourceReader(Path filePath, String urlString) {
        this.filePath = filePath;
        this.urlString = urlString;
    }

    public abstract DefaultDataFrame load() throws Exception;

    protected void validateSource() {
        if (filePath == null && urlString == null) {
            throw new NullPointerException("Either file path or URL must be provided");
        }
    }
}
