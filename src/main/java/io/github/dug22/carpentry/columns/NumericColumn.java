package io.github.dug22.carpentry.columns;

import io.github.dug22.carpentry.column.AbstractColumn;
import io.github.dug22.carpentry.column.numbers.NumericMapFunctions;
import io.github.dug22.carpentry.column.numbers.StatisticFunctions;

public abstract class NumericColumn<T extends Number> extends AbstractColumn<T> implements NumericMapFunctions<T>, StatisticFunctions<T> {

    public NumericColumn(String columnName, Class<T> columnType) {
        super(columnName, columnType);
    }

    public NumericColumn(String columnName, Class<T> columnType, T[] values) {
        super(columnName, columnType, values);
    }
}
